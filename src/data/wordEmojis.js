// Set of words that have verified emoji mappings
export const EMOJI_WORDS = new Set(Object.keys(WORD_EMOJIS));

export function getWordEmoji(word) {
  if (!word) return '📝';
  return WORD_EMOJIS[word.toLowerCase()] || '📝';
}

export function hasEmoji(word) {
  if (!word) return false;
  return EMOJI_WORDS.has(word.toLowerCase());
}

export function getWordSentence(word) {
  if (!word) return '';
  return WORD_SENTENCES[word.toLowerCase()] || `Look, a ${word}!`;
}
